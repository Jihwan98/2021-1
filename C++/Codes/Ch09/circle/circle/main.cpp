#include <iostream>
using namespace std;
// class의 정의와 class의 implementation 분리할 예정
// class 정의에는 member 변수, member 함수의 proto type 만 적는다.
// class implementation 파트에 멤버 함수를 정의한다.
class Circle {
public:
	double radius;
	Circle();
	Circle(double r);
	double getArea();
};
Circle::Circle() { // scope resolution operator
	cout << "no-arg constructor" << endl;
	radius = 1;

}
Circle::Circle(double r) {
	cout << "arg constructor" << endl;
	radius = r;
}
double Circle::getArea() {
	return radius * radius * 3.14;
}
int main() {
	cout << sizeof(Circle) << endl;
	Circle c1;
	cout << sizeof(c1) << endl;
	cout << "address of c1 : " << &c1 << endl;
	cout << "c1.radius : " << c1.radius << endl;
	cout << "c1 면적 : " << c1.getArea() << endl;
	// Circle c2(); // warning 프로토타입 함수
	Circle c3(5);
	cout << "address of c3 : " << &c3 << endl;
	cout << "c3 면적 : " << c3.getArea() << endl;
	c1 = c3; // assignment
	cout << "c1.radius : " << c1.radius << endl;

	return 0;
}